# defining strings in Python
# all of the following are equivalent
my_string = 'Hello'
print(my_string)  # Hello

my_string = "Hello"
print(my_string) # Hello

# triple code is used to specify/ to mention more than a single lien
my_string = '''Hello
                 Lalitha'''  # hello
                             # lalitha
print(my_string)

# triple quotes string can extend multiple lines
my_string = """Hello, welcome to
           the world of Python"""   # Hello, welcome to
                                    # the world of Python
print(my_string)
