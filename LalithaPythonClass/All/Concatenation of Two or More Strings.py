# Python String Operations
str1 = 'Hello'
str2 ='World!'

# using +
print('str1 + str2 = ', str1 + str2) # str1 + str2 =  HelloWorld!

# using *
print('str1 * 3 =', str1 * 3) # str1 * 3 = HelloHelloHello

# two string literals together
print('Hello''World!') # Hello World!

#using parentheses
s = ('Hello '
        'World') # Hello World
print(s)
