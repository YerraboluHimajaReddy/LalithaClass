# Changing and adding Dictionary Elements
my_dict = {'name': 'Jack', 'age': 26}
print(my_dict) #{'age': 26, 'name': 'Jack'}

# update value
my_dict['age'] = 27
print(my_dict) # {'age': 27, 'name': 'Jack'}

# add item
my_dict['address'] = 'Downtown'
print(my_dict) # {'name': 'Jack', 'age': 27, 'address': 'Downtown'}

my_dict['FullName'] = 'Lalitha'
print(my_dict) #{'address': 'Downtown', 'age': 27, 'FullName': 'Lalitha', 'name': 'Jack'}

